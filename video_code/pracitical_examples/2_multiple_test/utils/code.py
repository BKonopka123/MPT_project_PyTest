def arr_sort(arr):
    arr.sort()
    return arr

def arr_find(arr, x):
    return arr.index(x)

def arr_min(arr):
    return min(arr)

def arr_max(arr):
    return max(arr)

def arr_reverse(arr):
    new_arr = arr[::-1]
    return new_arr

def sum(a, b):
    return a+b

def diff(a, b):
    return a-b

def multi(a, b):
    return a*b

def div(a, b):
    return a/b